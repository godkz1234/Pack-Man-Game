void Position(int x, int y);

void Render(Character character, Character ghosts[], Item items[], int score) {
    system("cls");  // Clear the console

    for (int i = 0; i < HEIGHT; i++) {
        for (int j = 0; j < WIDTH; j++) {
            if (maze[i][j] == '0') {
                printf("  ");
            }
            else if (maze[i][j] == '1') {
                printf("■");
            }
        }
        printf("\n");
    }

    // 캐릭터 위치에 캐릭터 그리기
    Position(character.x, character.y);
    printf("%s", character.shape);

    // 고스트들 위치에 고스트 그리기
    for (int i = 0; i < GHOST_COUNT; i++) {
        Position(ghosts[i].x, ghosts[i].y);
        printf("%s", ghosts[i].shape);
    }

    // 활성화된 아이템 그리기
    for (int i = 0; i < ITEM_COUNT; i++) {
        if (items[i].active) {
            Position(items[i].x, items[i].y);
            printf("★");
        }
    }

    // 점수 표시
    Position(0, HEIGHT + 1);
    printf("Score: %d\n", score);
}

void Position(int x, int y) {
    COORD position = { x * 2, y };  // x를 2배로 하여 공백 고려
    SetConsoleCursorPosition(GetStdHandle(STD_OUTPUT_HANDLE), position);
}

void MoveGhost(Character* ghost, Character player, int* moveCounter) {
    (*moveCounter)++;
    if (*moveCounter % 3 != 0) {
        return;  // 고스트 속도 조절
    }

    if (ghost->x < player.x) {
        if (maze[ghost->y][ghost->x + 1] != '1') {
            ghost->x++;
        }
    }
    else if (ghost->x > player.x) {
        if (maze[ghost->y][ghost->x - 1] != '1') {
            ghost->x--;
        }
    }

    if (ghost->y < player.y) {
        if (maze[ghost->y + 1][ghost->x] != '1') {
            ghost->y++;
        }
    }
    else if (ghost->y > player.y) {
        if (maze[ghost->y - 1][ghost->x] != '1') {
            ghost->y--;
        }
    }
}

void GenerateItems(Item items[]) {
    srand(time(NULL));  // 랜덤 시드 설정

    int generated = 0;
    while (generated < ITEM_COUNT) {
        int x = rand() % WIDTH;
        int y = rand() % HEIGHT;

        if (maze[y][x] == '0') {  // 빈 공간에만 아이템을 생성
            items[generated].x = x;
            items[generated].y = y;
            items[generated].active = 1;
            generated++;
        }
    }
}

int CheckItemCollection(Character* player, Item items[]) {
    for (int i = 0; i < ITEM_COUNT; i++) {
        if (items[i].active && player->x == items[i].x && player->y == items[i].y) {
            items[i].active = 0;  // 아이템 비활성화
            return 1;  // 점수 1점 추가
        }
    }
    return 0;
}

int AllItemsCollected(Item items[]) {
    for (int i = 0; i < ITEM_COUNT; i++) {
        if (items[i].active) {
            return 0;  // 아직 남은 아이템이 있다면 0을 반환
        }
    }
    return 1;  // 모든 아이템이 수집되었다면 1을 반환
}

void GameOver() {
    Position(0, HEIGHT + 2);
    printf("Game Over! You were caught by a ghost!\n");
}

void Victory() {
    Position(0, HEIGHT + 2);
    printf("Victory! You collected all the items!\n");
}

int main() {
    Character player = { 1, 1, "▲" };  // 초기 위치 (1, 1)
    Character ghosts[GHOST_COUNT] = {  // 고스트 배열 생성
        { 20, 17, "●" },  // 고스트 1
        { 10, 9, "●" },  // 고스트 2
        { 25, 25, "●" }   // 고스트 3
    };
    Item items[ITEM_COUNT];  // 아이템 배열
    int score = 0;
    int moveCounter = 0;  // 고스트 이동을 제어하는 변수
    int playerMoved = 0;  // 플레이어가 처음 움직였는지 확인하는 변수

    GenerateItems(items);  // 아이템 생성

    char key = 0;

    while (1) {
        Render(player, ghosts, items, score);

        if (_kbhit()) {
            key = _getch();

            if (key == -32) {
                key = _getch();
            }

            switch (key) {
            case UP:
                if (maze[player.y - 1][player.x] != '1') player.y--;
                break;
            case LEFT:
                if (maze[player.y][player.x - 1] != '1') player.x--;
                break;
            case RIGHT:
                if (maze[player.y][player.x + 1] != '1') player.x++;
                break;
            case DOWN:
                if (maze[player.y + 1][player.x] != '1') player.y++;
                break;
            }

            playerMoved = 1;  // 플레이어가 움직였음을 표시
        }

        if (playerMoved) {
            for (int i = 0; i < GHOST_COUNT; i++) {
                MoveGhost(&ghosts[i], player, &moveCounter);  // 각 고스트가 플레이어를 추격
            }
        }

        // 아이템 수집 체크
        score += CheckItemCollection(&player, items);

        // 승리 조건 체크
        if (AllItemsCollected(items)) {
            Victory();  // 승리 함수 호출
            break;
        }

        // 고스트와 충돌 체크
        for (int i = 0; i < GHOST_COUNT; i++) {
            if (player.x == ghosts[i].x && player.y == ghosts[i].y) {
                GameOver();  // 게임 오버 함수 호출
                return 0;
            }
        }

        Sleep(100);  // 프레임 속도 조절
    }

    return 0;
}
